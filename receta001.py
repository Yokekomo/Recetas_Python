# Receta Python 001: Crear variables independientes para cada elemento de una lista o tupla.


""" Crear variables independientes para una tupla """

tupla = (1, 3)
a, b = tupla
print(a, b)

""" Crear variables independientes para una lista """

lista = ['Python', 3, (4, 5), 'P']
a, b, c, d = lista
print(a, b, c, d)